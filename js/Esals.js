function CalcularTrafico_flex() {
    var pt = $('#trafico_ptFlexible').val() * 1;
    var sn = $('#traficoSN_Flexible').val() * 1;

    $(tipoVehiculos).each(function (i, item) {
        
        CalcularF_Equivalencia_flex(item, pt, sn);

    });
}

